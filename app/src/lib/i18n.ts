const DICTS: Record<Lang, Dict> = { ko, en, ja, zh, vi };

const LS_LANG = "txtmyworld_lang";

function isLang(v: string | null): v is Lang {
  return v === "ko" || v === "en" || v === "ja" || v === "zh" || v === "vi";
}

let current: Lang = (() => {
  const stored = localStorage.getItem(LS_LANG);
  return isLang(stored) ? stored : "ko";
})();

const listeners = new Set<() => void>();

export function getLang(): Lang {
  return current;
}

export function setLang(lang: Lang) {
  current = lang;
  localStorage.setItem(LS_LANG, lang);
  document.documentElement.lang = lang;
  listeners.forEach((fn) => fn());
}

/** 앱 부트 시 <html lang>을 저장된 언어로 동기화 (렌더 전에 호출) */
export function bootstrapLang() {
  document.documentElement.lang = current;
}

function subscribe(fn: () => void) {
  listeners.add(fn);
  return () => listeners.delete(fn);
}

export function translate(lang: Lang, key: string, params?: Record<string, string | number>): string {
  let s = DICTS[lang][key] ?? DICTS.ko[key] ?? key;
  if (params) {
    for (const [k, v] of Object.entries(params)) {
      s = s.split(`{${k}}`).join(String(v));
    }
  }
  return s;
}

/** 언어 상태 구독 + 번역 함수. 컴포넌트에서 const { t, lang, setLang } = useI18n() */
export function useI18n() {
  const lang = useSyncExternalStore(subscribe, getLang);
  return {
    lang,
    setLang,
    t: (key: string, params?: Record<string, string | number>) => translate(lang, key, params),
  };
}
